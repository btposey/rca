"""generate_concierge_dataset.py — generate Concierge fine-tuning data using the live pipeline.

Each training sample is generated by:
  1. Running a synthetic query through the real Dispatcher (vLLM) to extract intent
  2. Running the extracted intent through the real Librarian (pgvector) to get candidates
  3. Having Claude (Opus via Bedrock) generate the gold response given those exact candidates

This ensures the training distribution exactly matches inference:
  - Real dispatcher outputs (not synthetic)
  - Real retrieved candidates from the actual restaurant database
  - Gold responses grounded in the actual candidate set

Prerequisites (must be running on GPU machine before invoking this script):
  - vLLM Dispatcher on port 8000
  - PostgreSQL with ingested restaurants on port 5432

Produces:
  data/synthetic/concierge_train.jsonl  (~500 samples)
  data/synthetic/concierge_eval.jsonl   (~60 held-out samples)

Usage (run from macOS dev machine with ports forwarded, or directly on GPU machine):
  uv run scripts/generate_concierge_dataset.py
  uv run scripts/generate_concierge_dataset.py --train-size 500 --eval-size 60
"""
