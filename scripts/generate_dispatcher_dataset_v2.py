"""generate_dispatcher_dataset_v2.py — improved Dispatcher training data.

Key improvements over v1:
  1. Attack samples generated by Claude (subtle, varied) not deterministic mutations
  2. Better cuisine/price extraction from implicit language ("hamburgers", "cheap", "splurge")
  3. More diverse attack patterns: embedded, authority, social engineering, indirect
  4. Streams to disk immediately — no data loss on crash

Produces:
  data/synthetic/train.jsonl   (~800 normal + ~200 attack)
  data/synthetic/eval.jsonl    (~100 held-out normal)

Usage:
  uv run scripts/generate_dispatcher_dataset_v2.py
"""
import json
import random
from pathlib import Path
from typing import Optional

import boto3
import instructor
import typer
from pydantic import BaseModel
from rich.console import Console
from rich.progress import track

from app.schema import DispatcherOutput, SearchPredicate

